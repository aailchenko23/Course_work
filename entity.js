function loadCity(cityKey) {
  const city = cities[cityKey];
  if (city) {
    document.getElementById("city_name").textContent = city.name;
    document.getElementById("city_description").textContent = city.description;
    document.body.style.backgroundImage = `url('${city.image}')`;
    document.body.style.backgroundSize = "cover";
    document.body.style.backgroundPosition = "center";
    document.body.style.backgroundRepeat = "no-repeat";

    // Оновити активний клас у меню
    document.querySelectorAll("[data-city]").forEach((link) => {
      if (link.dataset.city === cityKey) {
        link.classList.add("active");
      } else {
        link.classList.remove("active");
      }
    });
  }
}

// Обробка кліків по меню
document.addEventListener("DOMContentLoaded", () => {
  loadCity("lviv"); // початкове місто

  document.querySelectorAll("[data-city]").forEach((link) => {
    link.addEventListener("click", (event) => {
      event.preventDefault();
      const cityKey = link.dataset.city;
      loadCity(cityKey);
    });
  });
});
